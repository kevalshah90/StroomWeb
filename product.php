                     <h3 class="col-blue1 m-b-30"> 
                     Model Specification
                     </h3>
                     <p class="col-black">We perform a series of steps to train the model.</p>
                     <ul class="col-black"> 
                     <li>Split data into training and test. We use 80-20 split.</li>
                      <li>Check for bad, missing data. We use statistical distributions to identify outliers and use business logic to drop certain rows with bad data. In some cases, we use geographic proximity to impute missing data or replace bad data.</li>
                       <li>We use geohashes (location identifiers) in our models to control for some of the unobservables and location specific characteristics.</li>
                        <li>Categorical variables are encoded using OneHotEncoder.</li>
                         <li>Hyper parameters are tuned using GridSearch CV to obtain the best combination for variables, and subset of training data.</li>
                         <li>Metrics like Mean squared error and Mean Absolute Percentage Error are used to evaluate the model.</li>
                         </ul> </div>
